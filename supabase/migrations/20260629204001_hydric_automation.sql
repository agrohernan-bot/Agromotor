  ON public.hydric_events FOR UPDATE TO authenticated
  USING ((select auth.uid()) = user_id)
  WITH CHECK ((select auth.uid()) = user_id);

DROP POLICY IF EXISTS "hydric_events_delete_own" ON public.hydric_events;
CREATE POLICY "hydric_events_delete_own"
  ON public.hydric_events FOR DELETE TO authenticated
  USING ((select auth.uid()) = user_id);

