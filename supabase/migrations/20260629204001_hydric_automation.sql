  ON public.hydric_forecasts FOR SELECT TO authenticated
  USING ((select auth.uid()) = user_id);

DROP POLICY IF EXISTS "hydric_calibrations_select_own" ON public.hydric_calibrations;
CREATE POLICY "hydric_calibrations_select_own"
  ON public.hydric_calibrations FOR SELECT TO authenticated
  USING ((select auth.uid()) = user_id);

DROP POLICY IF EXISTS "hydric_latest_select_own" ON public.hydric_latest;
